print(abs(-5))  #   절댓값, absolute value
print(pow(4, 2))    #   4의 2승 = 4x4
print(max(5, 12))   #   최댓값 반환 즉, 12를 출력
print(min(5, 12))   #   최솟값 반환 즉, 5를 출력
print(round(3.14))  #   반올림 값 반환, 즉 3 나옴
print(round(4.90))  #   5

from math import *
print(floor(4.99))  #   내림    4   4.99에서 뒤의 소숫점을 모두 내림
print(ceil(3.14))   #   올림    4   3.14에서 뒤의 소숫점을 모두 올림 
print(sqrt(16))     #   제곱근  16의 제곱근 = 4